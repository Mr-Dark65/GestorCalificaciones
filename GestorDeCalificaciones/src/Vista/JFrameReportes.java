    private void cargarMaterias() {
        System.out.println("MATERIAS DISPONIBLES" + Almacen.getInstance().materias.size());
        for (Docente doce : Almacen.getInstance().informacion) {
            ArrayList<Materia> materias = doce.getMaterias();
            for (Materia materia : materias) {
                cbMaterias.addItem(materia.getNombre());
            }
        }
    }

    private void listar(){
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            modelo = (DefaultTableModel) tabla.getModel();
            Object[] objeto;
            for (Docente doc : Almacen.getInstance().informacion) {
                for (Materia materias : doc.getMaterias()) {
                    if (materias.getNombre().equals(cbMaterias.getSelectedItem().toString())) {
                        for (Estudiante e : materias.getEstudiante()) {
                            objeto = new Object[5];
                            objeto[0] = e.getNombre();
                            objeto[1] = e.getApellido();
                            objeto[2] = e.getCedula();
                            objeto[3] = e.getNota();
                            objeto[4] = e.getFecha();
                            modelo.addRow(objeto);
                        }
                    }

                }
            }
            tabla.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    /**
     * Metodo para limpiar la tabla
     */
    private void limpiarTabla() {
        DefaultTableModel tb = (DefaultTableModel) tabla.getModel();
        int a = tabla.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
    }

